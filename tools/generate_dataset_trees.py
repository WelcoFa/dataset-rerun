from __future__ import annotations

from pathlib import Path


REPO_ROOT = Path(__file__).resolve().parents[1]
DATA_ROOT = REPO_ROOT / "data"
OUTPUT_PATH = REPO_ROOT / "docs" / "dataset-trees.md"


DATASETS = [
    ("GigaHands", DATA_ROOT / "gigahands"),
    ("HOT3D", DATA_ROOT / "HOT3D"),
    ("Being-H0", DATA_ROOT / "Being-h0"),
    ("DexWild", DATA_ROOT / "dexwild"),
    ("ThermoHands", DATA_ROOT / "thermohands"),
    ("WIYH", DATA_ROOT / "wyih"),
]


def format_size(size_bytes: int) -> str:
    units = ["B", "KB", "MB", "GB", "TB"]
    value = float(size_bytes)
    for unit in units:
        if value < 1024.0 or unit == units[-1]:
            if unit == "B":
                return f"{int(value)} {unit}"
            return f"{value:.1f} {unit}"
        value /= 1024.0
    return f"{size_bytes} B"


def list_tree_lines(
    root: Path,
    *,
    max_depth: int = 4,
    max_entries_per_dir: int = 30,
    ignore_names: set[str] | None = None,
) -> list[str]:
    ignore_names = ignore_names or {".cache", ".venv", ".venv.venv", ".idea", "__pycache__"}

    def walk(path: Path, prefix: str, depth: int) -> list[str]:
        if depth > max_depth:
            return [f"{prefix}..."]

        try:
            entries = sorted(
                [p for p in path.iterdir() if p.name not in ignore_names],
                key=lambda p: (not p.is_dir(), p.name.lower()),
            )
        except PermissionError:
            return [f"{prefix}[permission denied]"]

        lines: list[str] = []
        visible = entries[:max_entries_per_dir]
        omitted = len(entries) - len(visible)

        for index, entry in enumerate(visible):
            connector = "`-- " if index == len(visible) - 1 and omitted == 0 else "|-- "
            label = entry.name + ("/" if entry.is_dir() else "")
            if entry.is_file():
                try:
                    label += f" ({format_size(entry.stat().st_size)})"
                except OSError:
                    pass
            lines.append(f"{prefix}{connector}{label}")
            if entry.is_dir():
                child_prefix = prefix + ("    " if connector == "`-- " else "|   ")
                lines.extend(walk(entry, child_prefix, depth + 1))

        if omitted > 0:
            lines.append(f"{prefix}`-- ... ({omitted} more entries)")

        return lines

    return [f"{root.name}/"] + walk(root, "", 1)


def summarize_hdf5_dataset(path: Path) -> list[str]:
    try:
        import h5py
    except ImportError:
        return [f"{path.name} ({format_size(path.stat().st_size)})", "  h5py not installed; cannot inspect groups."]

    lines = [f"{path.name} ({format_size(path.stat().st_size)})"]
    try:
        with h5py.File(path, "r") as f:
            top_keys = sorted(f.keys())
            for key in top_keys[:20]:
                obj = f[key]
                if isinstance(obj, h5py.Group):
                    lines.append(f"|-- {key}/")
                    child_keys = sorted(obj.keys())[:12]
                    for child_index, child_key in enumerate(child_keys):
                        child = obj[child_key]
                        last = child_index == len(child_keys) - 1
                        child_connector = "`-- " if last else "|-- "
                        if isinstance(child, h5py.Group):
                            lines.append(f"|   {child_connector}{child_key}/")
                        else:
                            lines.append(f"|   {child_connector}{child_key} shape={getattr(child, 'shape', None)}")
                    if len(obj.keys()) > len(child_keys):
                        lines.append(f"|   `-- ... ({len(obj.keys()) - len(child_keys)} more entries)")
                else:
                    lines.append(f"|-- {key} shape={getattr(obj, 'shape', None)}")
            if len(top_keys) > 20:
                lines.append(f"`-- ... ({len(top_keys) - 20} more top-level entries)")
    except Exception as exc:
        lines.append(f"  failed to inspect HDF5: {exc}")
    return lines


def dataset_tree(path: Path) -> list[str]:
    if not path.exists():
        return [f"{path.name}/", "└─ [missing]"]

    if path.name.lower() == "dexwild":
        hdf5_files = sorted(path.glob("*.hdf5")) + sorted(path.glob("*.h5"))
        if hdf5_files:
            return [f"{path.name}/", *[f"`-- {line}" for line in summarize_hdf5_dataset(hdf5_files[0])]]

    return list_tree_lines(path)


def build_markdown() -> str:
    sections = [
        "# Dataset Trees",
        "",
        "> Auto-generated by `tools/generate_dataset_trees.py`. Do not edit manually.",
        "",
        "These trees summarize the current local dataset layouts under `data/` and are intended to help adapter work and dataset onboarding.",
        "",
    ]

    for dataset_name, dataset_path in DATASETS:
        sections.extend(
            [
                f"## {dataset_name}",
                "",
                f"Path: `{dataset_path.relative_to(REPO_ROOT)}`",
                "",
                "```text",
                *dataset_tree(dataset_path),
                "```",
                "",
            ]
        )

    return "\n".join(sections)


def main():
    OUTPUT_PATH.write_text(build_markdown(), encoding="utf-8")
    print(f"Wrote {OUTPUT_PATH}")


if __name__ == "__main__":
    main()
